#Exercise 7 - Some Counting

#to count from 0 to 50 in increment 1

a = 0

while a <=50:
    print(a)
    a = a + 1

#to count from 50 to 0 decrement 1

b = 50

while b >=0:
    print(b)
    b = b - 1

#to count from 30 to 50 increment 1

c = 30

while c <=50:
    print(c)
    c = c + 1

#to count from 50 to 10 decrement 2

d = 50

while d >=10:
    print(d)
    d = d - 2

